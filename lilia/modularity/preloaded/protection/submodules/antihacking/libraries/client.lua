local MODULE = MODULE
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
local lastcheck
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function MODULE:Think()
    if not lastcheck then
        lastcheck = CurTime()
    end

    if CurTime() - lastcheck > 60 then
        local commands, _ = concommand.GetTable()
        for _, cmd in pairs(self.HackCommands) do
            if commands[cmd] then
                net.Start("IAmHackingOwO")
                net.SendToServer()
            end
        end

        for _, globalVar in pairs(self.MaliciousGlobals) do
            if _G[globalVar] then
                net.Start("IAmHackingOwO")
                net.SendToServer()
            end
        end

        lastcheck = CurTime()
    end
end

