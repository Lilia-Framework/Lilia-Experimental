function MODULE:OnPlayerDropWeapon(client, item, entity)
    local physObject = entity:GetPhysicsObject()
    if physObject then
        physObject:EnableMotion()
    end

    timer.Simple(
        lia.config.TimeUntilDroppedSWEPRemoved,
        function()
            if entity and entity:IsValid() then
                entity:Remove()
            end
        end
    )
end
