----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
local function ScreenScale(size)
    return size * (ScrH() / 900) + 10
end

----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function CharacterSelection:LiliaLoaded()
    vgui.Create("liaCharacter")
end

----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function CharacterSelection:KickedFromCharacter(_, isCurrentChar)
    if isCurrentChar then vgui.Create("liaCharacter") end
end

----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function CharacterSelection:CreateMenuButtons(tabs)
    tabs["characters"] = function(_)
        if IsValid(lia.gui.menu) then lia.gui.menu:Remove() end
        if self.KickOnEnteringMainMenu then netstream.Start("liaCharKickSelf") end
        vgui.Create("liaCharacter")
    end
end

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function CharacterSelection:LoadFonts(font)
    surface.CreateFont(
        "liaCharTitleFont",
        {
            font = font,
            weight = 200,
            size = ScreenScale(70),
            additive = true
        }
    )

    surface.CreateFont(
        "liaCharDescFont",
        {
            font = font,
            weight = 200,
            size = ScreenScale(24),
            additive = true
        }
    )

    surface.CreateFont(
        "liaCharSubTitleFont",
        {
            font = font,
            weight = 200,
            size = ScreenScale(12),
            additive = true
        }
    )

    surface.CreateFont(
        "liaCharButtonFont",
        {
            font = font,
            weight = 200,
            size = ScreenScale(24),
            additive = true
        }
    )

    surface.CreateFont(
        "liaCharSmallButtonFont",
        {
            font = font,
            weight = 200,
            size = ScreenScale(22),
            additive = true
        }
    )
end
