function MODULECheckDistance(client, ent)
    return ent:GetPos():DistToSqr(client:GetPos()) < lia.config.MaxInteractionDistance
end

--------------------------------------------------------------------------------------------------------------------------
function MODULE:AddPIMOption(name, data)
    self.options[name] = data
end
