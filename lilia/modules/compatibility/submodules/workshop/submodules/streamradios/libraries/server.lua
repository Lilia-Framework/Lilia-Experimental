function StreamRadiosCompatibility:SaveData()
    local savedTable = {}
    for k, v in ipairs(ents.FindByClass("sent_streamradio")) do
        table.insert(
            savedTable,
            {
                client = v.pl,
                mdl = v:GetModel(),
                pos = v:GetPos(),
                ang = v:GetAngles(),
                settings = v.SettingVar
            }
        )
    end

    self:setData(savedTable)
end
