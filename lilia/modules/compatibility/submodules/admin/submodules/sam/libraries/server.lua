function SAMCompatibility:PlayerSpawnRagdoll(ply)
    local limit = ply:GetLimit("ragdolls")
    if limit < 0 then return end
    local ragdolls = ply:GetCount("ragdolls") + 1
    if ragdolls > limit then
        ply:LimitHit("ragdolls")

        return false
    end
end
